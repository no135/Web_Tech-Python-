name = 'hanna'

print(name.capitalize())#capitalize the first letter

print(len(name))#return the length of string

print(name.isalpha())#check if all characters are alphabetic

print(name.isdigit())#check if all characters are digits

print(name.isalnum())#check if characters are letters + digits

print(name.islower())#check if character is lowercase

print(name.isupper())#check if character is uppercase

